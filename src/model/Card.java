package model;

public class Card {
	
	private int id;
	private String nombre;
	private int coste;
	private int ataque;
	private int defensa;
	private int valor;
	public int getId() {
		return id;
	}
	public String getNombre() {
		return nombre;
	}
	public int getCoste() {
		return coste;
	}
	public int getAtaque() {
		return ataque;
	}
	public int getDefensa() {
		return defensa;
	}
	public int getValor() {
		return valor;
	}
}
